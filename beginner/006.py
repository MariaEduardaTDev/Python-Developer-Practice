N1 = int(input('Digite um número inteiro: '))
N2 = float(input('Digite um número decimal: '))

print('O dobro de {} é {} \n O dobro de {} é {}' .format(N1, N1*2, N2, N2*2))
print('O triplo de {} é {} \n O triplo {} é {}' .format(N1, N1*3, N2, N2*3))
print('A raiz quadrada de {} é {} \n A raiz quadrada de {} é {}' .format(N1, N1**(1/2), N2, N2**(1/2)))